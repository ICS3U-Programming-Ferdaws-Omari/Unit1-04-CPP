// This program shows the dimensions of a rectangle
// and calculates the area and perimeter of it.


#include <iostream>

int main() {
  std::cout << "If a rectangle has the dimensions: " << std::endl;
  std::cout << "4cm x 2cm" << std::endl;
  std::cout << "" << std::endl;
  std::cout << "Area is:" << (4*2) << "cm^2" << std::endl;
  std::cout << "Perimeter is:" << (2*(4+2)) << "cm" << std::endl;
}